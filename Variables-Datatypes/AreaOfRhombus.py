#  Create a Python program that calculates the area of a rhombus using variables for diagonals

"""
A = (d₁ × d₂) / 2

Where:

A is the area of the rhombus.
"d₁" and "d₂" are the lengths of the diagonals of the rhombus. The diagonals are the line segments that connect opposite vertices of the rhombus.
"""
LengthOfDiagonal1 = float(input("Write the length of th first diagonal: "))
LengthOfDiagonal2 = float(input("Write the length of th second diagonal: "))
